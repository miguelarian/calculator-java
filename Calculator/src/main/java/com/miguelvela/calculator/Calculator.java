package com.miguelvela.calculator;

public class Calculator {
    public double sum(double operatorA, double operatorB) {
        return operatorA + operatorB;
    }
    public double sub(double operatorA, double operatorB) {
        return operatorA - operatorB;
    }
    public double divide(double operatorA, double operatorB) {
        return operatorA / operatorB;
    }
    public double multiply(double operatorA, double operatorB) {
        return operatorA * operatorB;
    }
}
